# üìç –î–∞–Ω–∞ —Ñ—É–Ω–∫—Ü–∏—è f(x) = -12x^4 - 18x^3+5x^2 + 10x - 30

# üìù –û–ø—Ä–µ–¥–µ–ª–∏—Ç—å –∫–æ—Ä–Ω–∏

from sympy import *

x = Symbol('x')
func=-12*x**4+18*x**3+5*x**2+10*x-30
y=solve(func)
x1=float(y[0])
x2=float(y[1])
print(x1,x2)

# üìù –ù–∞–π—Ç–∏ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã, –Ω–∞ –∫–æ—Ç–æ—Ä—ã—Ö —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑—Ä–∞—Å—Ç–∞–µ—Ç

fd=diff(func)
print(solve(0<fd))

# üìù –ù–∞–π—Ç–∏ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã, –Ω–∞ –∫–æ—Ç–æ—Ä—ã—Ö —Ñ—É–Ω–∫—Ü–∏—è —É–±—ã–≤–∞–µ—Ç

print(solve(fd<0))

